#include "lib_math.h"

double _log(double exp) {
    return log(exp);
}

double _log10(double exp) {
    return log10(exp);
}

/* Unused */
float _log_float(float exp) {
    return logf(exp);
}

double _pow(double base, double exponent) {
    return pow(base, exponent);
}

double _sqrt(double sq) {
    return sqrt(sq);
}

double _sin(double deg) {
    return sin(deg);
}

double _cos(double deg) {
    return cos(deg);
}

double _tan(double deg) {
    return tan(deg);
}

long _ceil(double x) {
    return ceil(x);
}

long _floor(double x) {
    return floor(x);
}